//Write a program that takes two numbers and displays their sum, difference, product, and quotient.

//Declare and initialize two variables with number values
const num1 = Number(11);
const num2 = Number(15);

//Declare and initialize four more variables with arithmetic operations performed on num1 and num2
//Add num1 and num2
const sum = num1 + num2;
console.log(`Sum: ${sum}`);

//Subtract num2 from num1
const diff = num2 - num1;
console.log(`Difference: ${diff}`);

//Multiply num1 and num2
const product = num1 * num2;
console.log(`Product: ${product}`);

//Divide num1 by num2
const quotient = num1 / num2;
console.log(`Quotient: ${quotient}`);

